                  Vous êtes candidat au programme CCI ?
Dans un souci de faciliter votre compréhension de la soumission d'un dossier de candidature au programme CCI, nous vous invitons à suivre les étapes édictés ci-dessous :

<li > L’université (les autorités) autorise le dépôt du dossier du candidat.</li>
 <li>Le candidat prépare les éléments de son dossier (une déclinaison physique & numérique).</li>
 <li>Le candidat transmet la version physique du dossier à l’administration de son institution (Commission locale) ---Dossier alternatif.</li>
  <li>Le candidat crée un compte ou utilise son compte (recommandation d’utiliser un unique compte sur la plateforme) pour la soumission des pièces constitutives de son e-dossier ---Dossier principal</li>
<li>Le candidat transmet son e-dossier à son institution via la plateforme (Utilisez la rubrique et le bouton dédiés à cet effet sur la plateforme) ---Action importante</li>
<li>La commission institutionnelle accède au dossier du candidat (uniquement les dossiers transmis au point 5 seront visibles par la commission de votre institution)</li>
<li>La commission valide la conformité de votre e-dossier (Remplir les conditions "autorisation de votre institution" et "complétude de pièces exigées") et transmet le dossier au Président/Recteur/Directeur de l’institution. </li>
<li>Le Président/Recteur/Directeur confirme votre candidature et la transmet au Secrétariat Général du CAMES. </li>
<li>Le Secrétariat général transmet, via votre email, un accusé de réception (la notice individuelle du candidat générée à partir des pièces constitutives de son e-dossier sur la plateforme) avec le visa de l’institution. </li>
Fin de la phase de pré-inscription à une session du programme Comité Consultatif Interafricains (CCI) . </li>
